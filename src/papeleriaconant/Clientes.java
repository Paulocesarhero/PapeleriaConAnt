    private void setModelo() {
        String[] cabecera = {"Id", "Nombre", "Apellido", "edad", "Direccion","Tel","Correo"};
        modelo.setColumnIdentifiers(cabecera);
        usuariosTbl.setModel(modelo);

    }
    //agregamos los datos que existan en la lista en la tabla
    private void setDatos() {
        Object[] datos = new Object[modelo.getColumnCount()];
        modelo.setRowCount(0);
        for (Usuarios theprod : listaUsuarios) {
            datos[0] = theprod.getIdCliente();
            datos[1] = theprod.getNombre();
            datos[2] = theprod.getApellido();
            datos[3] = theprod.getEdad();
            datos[4] = theprod.getDireccion();
            datos[5] = theprod.getNumeroDeTelefono();
            datos[6] = theprod.getCorreoElectronico();
            modelo.addRow(datos);
        }
        usuariosTbl.setModel(modelo);
    }
    private void escribirLista() {
        try {
            ObjectOutputStream escribiendoFichero = new ObjectOutputStream(
                    new FileOutputStream(ruta));
            escribiendoFichero.writeObject(listaUsuarios);
            escribiendoFichero.close();
            archivoVacio = false;
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }

    }
    private void leerlista() {
        try {
            ObjectInputStream leyendoFichero = new ObjectInputStream(
                    new FileInputStream(ruta));
            listaUsuarios = (ArrayList<Usuarios>) leyendoFichero.readObject();
            leyendoFichero.close();
        } catch (FileNotFoundException e) {
            e.printStackTrace();
        } catch (IOException e) {
            e.printStackTrace();
        } catch (ClassNotFoundException e) {
            e.printStackTrace();
        }
    }

