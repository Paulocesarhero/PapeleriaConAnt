    private void EliminarUSuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EliminarUSuarioActionPerformed
        // TODO add your handling code here:
        if (usuariosTbl.getSelectedRow() == -1){
            JOptionPane.showMessageDialog(this,"Fila no seleccionada");
        }else{
            listaUsuarios.remove(usuariosTbl.getSelectedRow());
            modelo.removeRow(usuariosTbl.getSelectedRow());
            escribirLista();
        }
    }//GEN-LAST:event_EliminarUSuarioActionPerformed

    private void modificarUsuarioBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modificarUsuarioBtnActionPerformed
        // TODO add your handling code here:
        int filaSeleccionada = usuariosTbl.getSelectedRow();
        if (filaSeleccionada>=0){
            AgregarClientes modificar = new AgregarClientes(filaSeleccionada,ruta);
            modificarEliminarPanel.add(modificar);
            modificar.show();
        }else
            JOptionPane.showMessageDialog(this,"Fila no seleccionada");
    }//GEN-LAST:event_modificarUsuarioBtnActionPerformed

